package com.uranus.platform.business.pub.entity.po;

public class CallbackLogData {
    private String logId;

    private String transNo;

    private String busSide;

    private String txCode;

    private String callbackSts;

    private String reqDate;

    private String reqTime;

    private String respTime;

    private String reqContent;

    private String respContent;

    
    public CallbackLogData(String transNo, String busSide, String txCode, String callbackSts, String reqDate,
			String reqTime, String respTime, String reqContent, String respContent) {
		super();
		this.transNo = transNo;
		this.busSide = busSide;
		this.txCode = txCode;
		this.callbackSts = callbackSts;
		this.reqDate = reqDate;
		this.reqTime = reqTime;
		this.respTime = respTime;
		this.reqContent = reqContent;
		this.respContent = respContent;
	}

	public CallbackLogData() {
		super();
	}

	public String getLogId() {
        return logId;
    }

    public void setLogId(String logId) {
        this.logId = logId;
    }

    public String getTransNo() {
        return transNo;
    }

    public void setTransNo(String transNo) {
        this.transNo = transNo;
    }

    public String getBusSide() {
        return busSide;
    }

    public void setBusSide(String busSide) {
        this.busSide = busSide;
    }

    public String getTxCode() {
        return txCode;
    }

    public void setTxCode(String txCode) {
        this.txCode = txCode;
    }

    public String getCallbackSts() {
        return callbackSts;
    }

    public void setCallbackSts(String callbackSts) {
        this.callbackSts = callbackSts;
    }

    public String getReqDate() {
        return reqDate;
    }

    public void setReqDate(String reqDate) {
        this.reqDate = reqDate;
    }

    public String getReqTime() {
        return reqTime;
    }

    public void setReqTime(String reqTime) {
        this.reqTime = reqTime;
    }

    public String getRespTime() {
        return respTime;
    }

    public void setRespTime(String respTime) {
        this.respTime = respTime;
    }

    public String getReqContent() {
        return reqContent;
    }

    public void setReqContent(String reqContent) {
        this.reqContent = reqContent;
    }

    public String getRespContent() {
        return respContent;
    }

    public void setRespContent(String respContent) {
        this.respContent = respContent;
    }
}