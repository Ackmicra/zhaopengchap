package com.uranus.platform.business.pub.entity.po;

import lombok.Data;

@Data
public class WsBaseData {
    private String wsBaseId;

    private String wsDate;

    private String wsTime;

    private String txCode;

    private String busSide;

    private String wsSerial;

    private String respTime;

    private String respCode;

    private String respDesc;

    private String wsSts;

    private String brNo;

    private String reqContent;

    private String respContent;

	public WsBaseData() {
		super();
	}

	public WsBaseData(String wsDate, String wsTime, String txCode, String busSide, String wsSerial, String respTime,
			String respCode, String respDesc, String wsSts, String brNo, String reqContent, String respContent) {
		super();
		this.wsDate = wsDate;
		this.wsTime = wsTime;
		this.txCode = txCode;
		this.busSide = busSide;
		this.wsSerial = wsSerial;
		this.respTime = respTime;
		this.respCode = respCode;
		this.respDesc = respDesc;
		this.wsSts = wsSts;
		this.brNo = brNo;
		this.reqContent = reqContent;
		this.respContent = respContent;
	}
}