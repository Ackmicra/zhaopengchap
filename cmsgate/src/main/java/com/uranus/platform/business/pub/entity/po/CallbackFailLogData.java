package com.uranus.platform.business.pub.entity.po;

public class CallbackFailLogData {
    private String failId;

    private String transNo;

    private String busSide;

    private String txCode;

    private String reqDate;

    private String reqTime;
    
    public CallbackFailLogData(String transNo, String busSide, String txCode, String reqDate, String reqTime) {
		super();
		this.transNo = transNo;
		this.busSide = busSide;
		this.txCode = txCode;
		this.reqDate = reqDate;
		this.reqTime = reqTime;
	}

	public CallbackFailLogData() {
		super();
	}

	public String getFailId() {
        return failId;
    }

    public void setFailId(String failId) {
        this.failId = failId;
    }

    public String getTransNo() {
        return transNo;
    }

    public void setTransNo(String transNo) {
        this.transNo = transNo;
    }

    public String getBusSide() {
        return busSide;
    }

    public void setBusSide(String busSide) {
        this.busSide = busSide;
    }

    public String getTxCode() {
        return txCode;
    }

    public void setTxCode(String txCode) {
        this.txCode = txCode;
    }

    public String getReqDate() {
        return reqDate;
    }

    public void setReqDate(String reqDate) {
        this.reqDate = reqDate;
    }

    public String getReqTime() {
        return reqTime;
    }

    public void setReqTime(String reqTime) {
        this.reqTime = reqTime;
    }
}