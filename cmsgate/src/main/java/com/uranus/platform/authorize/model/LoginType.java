public enum LoginType {
    /**
     * 以网页形式登陆
     */
    WEB("web"),
    /**
     * 以手机形式登陆
     */
    MOBILE("mobile");

    private String loginType;
    LoginType(String loginType) {
        this.loginType = loginType;
    }

    public static LoginType defaultType(){
        return LoginType.WEB;
    }
}
